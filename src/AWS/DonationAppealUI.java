    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField2.getText().isEmpty() || this.checkAmount() == false || jComboBox0.getSelectedIndex() == 0 || jComboBox1.getSelectedIndex() == 0)
        {
            if(jTextField2.getText().isEmpty())
                this.jError1.setText("*Please Enter Amount");
            else
                this.checkAmount();
            
            if(jComboBox0.getSelectedIndex() == 0)
                this.jError2.setText("*Please Select Cause");
            
            if(jComboBox1.getSelectedIndex() == 0)
                this.jError3.setText("*Please Select Service Provider");
        }
        else
        {
            this.jError1.setText("");
            this.jError2.setText("");
            DAppeal1.setVisible(false);
            String cause = null, spUsername = null;
            double amount = 0;
            amount = Integer.parseInt(jTextField2.getText());
            cause = (String) jComboBox0.getSelectedItem();
            spUsername = (String) jComboBox1.getSelectedItem();
            mgr.getAppealManager().addDonationAppeal("DonateAppeal.csv", amount, cause, spUsername);
            DAppeal2.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

