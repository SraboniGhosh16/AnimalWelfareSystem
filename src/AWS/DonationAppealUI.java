    private boolean checkAmount(){
        try{
            double x = Double.parseDouble(jTextField2.getText());
            if(x <= 0 || x >= 50000){
                this.jError1.setText("*Invalid Amount. Limit upto 50000");
                return false;
            }
            else{
                this.jError1.setText("");
                return true;
            }
        }
        catch (NumberFormatException e){
            this.jError1.setText("*Invalid. Enter Digit Only");
            return false;
        }
    }
