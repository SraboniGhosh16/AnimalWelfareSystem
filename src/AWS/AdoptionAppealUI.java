    private boolean checkAge(){
        try{
            int x = Integer.parseInt(jTextField2.getText());
            if(x <= 0 || x >= 20){
                this.jError4.setText("*Invalid Age. Age Limit 1-20");
                return false;
            }
            else{
                this.jError4.setText("");
                return true;
            }
        }
        catch (NumberFormatException e){
            this.jError4.setText("*Invalid. Enter Digit Only");
            return false;
        }
    }
    
