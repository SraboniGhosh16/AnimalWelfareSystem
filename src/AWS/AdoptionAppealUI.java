    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jComboBox0.getSelectedIndex() == 0 || jComboBox1.getSelectedIndex() == 0 || jComboBox2.getSelectedIndex() == 0 || jTextField2.getText().isEmpty() || this.checkAge() == false || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty() || jComboBox3.getSelectedIndex() == 0)
        {
            if(jComboBox0.getSelectedIndex() == 0)
                this.jError1.setText("*Please Select Animal");
            
            if(jComboBox1.getSelectedIndex() == 0)
                this.jError2.setText("*Please Select Service Provider");
            
            if(jComboBox2.getSelectedIndex() == 0)
                this.jError3.setText("*Please Select Location");
            
            if(jTextField2.getText().isEmpty())
                this.jError4.setText("*Please Enter Age");
            else
                this.checkAge();
            
            if(jTextField3.getText().isEmpty())
                this.jError5.setText("*Please Enter Need");
            else
                this.jError5.setText("");
            
            if(jTextField4.getText().isEmpty())
                this.jError6.setText("*Please Enter Disease Name otherwise NA");
            else
                this.jError6.setText("");
            
            if(jComboBox3.getSelectedIndex() == 0)
                this.jError7.setText("*Please Select Adoption Criteria");
        }
        else
        {
            this.jError4.setText("");
            this.jError5.setText("");
            this.jError6.setText("");
            AAppeal1.setVisible(false);
            String animal = null, location = null, needs = null, diseases = null, criteria = null, spUsername = null;
            int age = 0;
            animal = (String) jComboBox0.getSelectedItem();
            spUsername = (String) jComboBox1.getSelectedItem();
            location = (String) jComboBox2.getSelectedItem();
            age = Integer.parseInt(jTextField2.getText());
            needs = jTextField3.getText();
            diseases = jTextField4.getText();
            criteria = (String) jComboBox3.getSelectedItem();
            mgr.getAppealManager().addAdoptionAppeal("AdoptAppeal.csv", animal, spUsername, age, location, needs, diseases, criteria);
            AAppeal2.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

